#include<bits/stdc++.h>

using namespace std;

int main()
{
    int x;

    cin>>x;

    if(x==0)
        cout<<"Zero"<<endl;
    else if(x==1)
        cout<<"One"<<endl;
    else if(x==2)
        cout<<"Two"<<endl;
    else if(x==3)
        cout<<"Three"<<endl;
    else if(x==4)
        cout<<"Four"<<endl;
    else if(x==5)
        cout<<"Five"<<endl;
    else if(x==6)
        cout<<"Six"<<endl;
    else if(x==7)
        cout<<"Seven"<<endl;
    else if(x==8)
        cout<<"Eight"<<endl;
    else if(x==9)
        cout<<"Nine"<<endl;
    else
        cout<<"Invalid Input"<<endl;


    return 0;
}


